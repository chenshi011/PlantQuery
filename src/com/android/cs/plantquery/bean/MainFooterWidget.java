package com.android.cs.plantquery.bean;

/**
 * 底部部件
 * @author chenshi
 *
 */
public class MainFooterWidget {
	
	private String id;
	private int index;
	private String name;
	private int icon;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIcon() {
		return icon;
	}
	public void setIcon(int icon) {
		this.icon = icon;
	}
	
}
